    <h4 class="alert-heading">Hikmah Zakat</h4>
    <p>Zakat bukan hanya kewajiban, tetapi juga memiliki banyak hikmah, baik bagi individu maupun masyarakat. Berikut adalah beberapa hikmah utama dari zakat:</p>

    <h5>1. Hikmah bagi Pemberi Zakat</h5>
    <ul>
        <li>✅ <strong>Menyucikan Harta</strong> → Zakat membersihkan harta dari hak orang lain dan menjadikannya lebih berkah.</li>
        <li>✅ <strong>Menumbuhkan Sifat Dermawan</strong> → Mengajarkan keikhlasan, kepedulian, dan menghindarkan dari sifat kikir.</li>
        <li>✅ <strong>Menghapus Dosa</strong> → Dalam hadis disebutkan bahwa zakat dapat menghapus dosa seperti air yang memadamkan api.</li>
        <li>✅ <strong>Menambah Rezeki & Keberkahan</strong> → Allah menjanjikan balasan berlipat bagi orang yang bersedekah dan berzakat.</li>
        <li>✅ <strong>Mendekatkan Diri kepada Allah</strong> → Sebagai bentuk ketaatan kepada perintah Allah.</li>
    </ul>

    <h5>2. Hikmah bagi Penerima Zakat</h5>
    <ul>
        <li>✅ <strong>Membantu Orang yang Kurang Mampu</strong> → Fakir miskin mendapatkan bantuan untuk memenuhi kebutuhan dasar mereka.</li>
        <li>✅ <strong>Mengurangi Kesenjangan Sosial</strong> → Zakat membantu menciptakan keseimbangan ekonomi dalam masyarakat.</li>
        <li>✅ <strong>Memberdayakan Kaum Lemah</strong> → Zakat bisa digunakan untuk modal usaha atau pendidikan agar mereka mandiri.</li>
    </ul>

    <h5>3. Hikmah bagi Masyarakat & Ekonomi</h5>
    <ul>
        <li>✅ <strong>Meningkatkan Solidaritas Sosial</strong> → Masyarakat jadi lebih peduli terhadap sesama.</li>
        <li>✅ <strong>Mengurangi Kemiskinan</strong> → Dengan distribusi yang tepat, zakat bisa membantu mengurangi angka kemiskinan.</li>
        <li>✅ <strong>Mendorong Perputaran Ekonomi</strong> → Harta tidak hanya berputar di kalangan orang kaya, tapi juga tersebar ke seluruh lapisan masyarakat.</li>
        <li>✅ <strong>Menjaga Stabilitas Sosial</strong> → Mengurangi kesenjangan sosial yang bisa menyebabkan konflik atau kriminalitas.</li>
    </ul>

